package SnakePj.controller;

//class that hold the point
public class Corner {
    int x;
    int y;

    public Corner(int x, int y) {
        this.x = x;
        this.y = y;
    }

    //x get
    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }


}
